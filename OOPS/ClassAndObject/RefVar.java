package ClassAndObject;

public class RefVar {
    // by using reference variable
   String color;
   int age;
   int height;
  
   

   public static void main(String args[]){
      RefVar v = new RefVar();
        v.color = "green";
        v.age = 20;
        v.height = 5;
        System.out.println(v.color );
        System.out.println(v.age );
        System.out.println(v.height);
        


        
   }
}

